);

CREATE TABLE CLIENTES (
ID INT AUTO_INCREMENT PRIMARY KEY,
NOME_CLI VARCHAR(255) NOT NULL,
TELEFONE VARCHAR(15) NOT NULL,
DATA DATE NOT NULL,
HORARIO TIME NOT NULL,
ID_PROC INT NOT NULL,
ID_STATUS INT NOT NULL
);

